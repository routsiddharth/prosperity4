        "print(\"=== Variance explained per PC ===\")\n",
        "display(explained.round(4))\n",
        "\n",
        "load_df = pd.DataFrame(loadings,\n",
        "                       index=[f\"PC{i+1}\" for i in range(P)],\n",
        "                       columns=columns)\n",
        "print(\"\\n=== Loadings (rows: PCs, cols: products) ===\")\n",
        "display(load_df.round(3))\n",
        "\n",
        "# Reconstruct PC residuals at the LEVEL (cumulative sum of returns gives a level-like series)\n",
