        "rets_kend = rets.sample(n=kend_subsample, random_state=RANDOM_SEED).sort_index() if len(rets) > kend_subsample else rets\n",
        "corr_kendall = rets_kend.corr(method=\"kendall\")\n",
        "\n",
        "\n",
        "def pair_long(C: pd.DataFrame, val_name: str) -> pd.DataFrame:\n",
        "    rows = []\n",
        "    cols = list(C.columns)\n",
        "    for i, a in enumerate(cols):\n",
        "        for j, b in enumerate(cols):\n",
        "            if i >= j:\n",
        "                continue\n",
        "            rows.append({\"a\": a, \"b\": b, val_name: C.iat[i, j]})\n",
        "    return pd.DataFrame(rows)\n",
        "\n",
        "\n",
        "p_level = pair_long(corr_level,    \"pearson_level\")\n",
        "p_ret   = pair_long(corr_ret,      \"pearson_ret\")\n",
        "p_spear = pair_long(corr_spearman, \"spearman_ret\")\n",
        "p_kend  = pair_long(corr_kendall,  \"kendall_ret\")\n",
        "\n",
        "both = p_level.merge(p_ret, on=[\"a\", \"b\"]) \\\n",
        "              .merge(p_spear, on=[\"a\", \"b\"]) \\\n",
