        "def best_integer_combo(S):\n",
        "    Gs = G[np.ix_(S, S)]\n",
        "    eigvals, eigvecs = np.linalg.eigh(Gs)\n",
        "    v = eigvecs[:, 0]\n",
        "    if np.max(np.abs(v)) == 0:\n",
        "        return None, np.inf, np.inf\n",
        "    scale = INT_COEF_MAX / np.max(np.abs(v))\n",
        "    near = np.clip(np.round(v * scale).astype(int), INT_COEF_MIN, INT_COEF_MAX)\n",
        "    best_c, best_v = None, np.inf\n",
        "    for d in iproduct([-1, 0, 1], repeat=len(S)):\n",
        "        c = np.clip(near + np.array(d), INT_COEF_MIN, INT_COEF_MAX)\n",
        "        if (c == 0).all():\n",
        "            continue\n",
        "        if (c != 0).sum() < 2:\n",
        "            continue\n",
        "        v_combo = c @ Gs @ c\n",
        "        if v_combo < best_v:\n",
        "            best_v, best_c = v_combo, c.copy()\n",
        "    if best_c is None:\n",
        "        return None, np.inf, np.inf\n",
        "    norm_var = sum((best_c[i] ** 2) * var[S[i]] for i in range(len(S)))\n",
        "    var_ratio = float(best_v / max(norm_var, 1e-12))\n",
        "    return best_c, float(best_v), var_ratio\n",
        "\n",
        "\n",
        "pure_rows = []\n",
        "for k in range(2, P + 1):\n",
        "    for S in combinations(range(P), k):\n",
        "        S_l = list(S)\n",
        "        c_int, v_combo, vr = best_integer_combo(S_l)\n",
        "        if c_int is None:\n",
        "            continue\n",
        "        spread = X[:, S_l] @ c_int.astype(float)\n",
        "        pure_rows.append({\n",
        "            \"k\":                 k,\n",
        "            \"basket\":            [columns[s] for s in S_l],\n",
        "            \"int_coefs\":         [int(c) for c in c_int],\n",
        "            \"basket_str\":        fmt_basket([columns[s] for s in S_l], list(c_int.astype(float))),\n",
        "            \"max_abs_coef\":      int(max(abs(c) for c in c_int)),\n",
        "            \"combo_var\":         float(v_combo),\n",
        "            \"var_ratio\":         vr,\n",
        "            \"adf_p\":             adf_p(spread),\n",
        "            \"half_life\":         half_life(spread),\n",
        "        })\n",
        "pure_table = pd.DataFrame(pure_rows).sort_values([\"adf_p\", \"var_ratio\"]).reset_index(drop=True)\n",
        "pure_table.to_csv(OUT_DIR / \"pure_integer_combos.csv\", index=False)\n",
        "\n",
