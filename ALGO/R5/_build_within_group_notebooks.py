        "                         columns=[f\"PC{i+1}\" for i in range(P)])\n",
        "\n",
        "pc_rows = []\n",
        "for i in range(P):\n",
        "    name   = f\"PC{i+1}\"\n",
        "    score  = scores[:, i]\n",
        "    level  = pc_levels[name].values\n",
        "    pc_rows.append({\n",
        "        \"pc\":                  name,\n",
        "        \"explained_var_ratio\": float(pca.explained_variance_ratio_[i]),\n",
        "        \"loadings\":            load_df.loc[name].round(3).to_dict(),\n",
        "        \"adf_p_score\":         adf_p(score),         # returns-space (should be small for stationary noise)\n",
        "        \"adf_p_level\":         adf_p(level),         # cumulative — small means stationary, NOT random walk\n",
        "        \"half_life_level\":     half_life(level),\n",
        "    })\n",
        "pc_table = pd.DataFrame(pc_rows)\n",
        "pc_table.to_csv(OUT_DIR / \"pca_residuals.csv\", index=False)\n",
        "print(\"\\n=== PC residual stationarity ===\")\n",
        "display(pc_table)\n",
        "\n",
        "# Plot the level-space PC trajectories\n",
        "fig = make_subplots(rows=P, cols=1, shared_xaxes=True, vertical_spacing=0.03,\n",
        "                    subplot_titles=[f\"PC{i+1}  (var={pca.explained_variance_ratio_[i]:.2%})\"\n",
        "                                    for i in range(P)])\n",
        "for i in range(P):\n",
        "    fig.add_trace(go.Scatter(y=pc_levels.iloc[:, i].values, line=dict(width=0.8),\n",
        "                             name=f\"PC{i+1}\"), row=i+1, col=1)\n",
