        "    \"\"\"ADF p-value. Optionally subsample for speed.\"\"\"\n",
        "    if subsample is None:\n",
        "        subsample = SUBSAMPLE_FOR_ADF\n",
        "    s = pd.Series(series).dropna().values\n",
        "    if len(s) < 30 or np.std(s) == 0:\n",
        "        return np.nan\n",
        "    if subsample and len(s) > subsample:\n",
        "        idx = np.linspace(0, len(s) - 1, subsample).astype(int)\n",
        "        s = s[idx]\n",
        "    try:\n",
        "        return float(adfuller(s, autolag=\"AIC\")[1])\n",
        "    except Exception:\n",
        "        return np.nan\n",
        "\n",
        "\n",
        "def half_life(series):\n",
        "    \"\"\"OU half-life in ticks. NaN if non-reverting.\"\"\"\n",
        "    s = pd.Series(series).dropna().values\n",
        "    if len(s) < 50:\n",
        "        return np.nan\n",
        "    x_lag = s[:-1]\n",
        "    dx    = np.diff(s)\n",
        "    X     = np.column_stack([np.ones_like(x_lag), x_lag])\n",
        "    try:\n",
        "        b = np.linalg.lstsq(X, dx, rcond=None)[0][1]\n",
